const long M = 998244353; // modulo
map<long, long> F;

long f(long n) {
	if (F.count(n)) return F[n];
	long k=n/2;
	if (n%2==0) { // n=2*k
		return F[n] = (f(k)*f(k) + f(k-1)*f(k-1)) % M;
	} else { // n=2*k+1
		return F[n] = (f(k)*f(k+1) + f(k-1)*f(k)) % M;
	}
}

int main(){
    int T; cin >> T;
    while(T--){
        long N, x, y; cin>>N>>x>>y;
        F[0] = x;
        F[1] = y;
        long product = 1;
    for (int i = 0; i < N; i++) {
        product = (product * f(i)) % M;
    }
        cout<<product<<endl;
    }
}
